// Bucle For
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// Buble For of
let arr: number[] = [1, 2, 3, 4, 5];
for (let num of arr) {
    console.log(num);
}

// Bucle while
let count: number = 0;
while (count < 5) {
    console.log(count);
    count++;
}

// Bucle do while
let num: number = 0;
do {
    console.log(num);
    num++;
} while (num < 5);
